    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        try {
            
            int bemSucedido = controlaUsuario.validaLogin(txtUsuario.getText(),
                    Validacao.criptografarSenha(txtSenha.getText()));
            
            if (bemSucedido == 1) {
                TelaPrincipal telaPrincipal = new TelaPrincipal(controlaUsuario.getUsuarioLogado());
                telaPrincipal.setVisible(true);
                this.dispose();
            } else if (bemSucedido == 0) {
                JOptionPane.showMessageDialog(this, "USUARIO OU SENHA INCORRETO!");
            } else {
                JOptionPane.showMessageDialog(this, "ERRO DE CONEXAO, CHAME O SUPORTE");
            }

        } catch (NoSuchAlgorithmException e) {
            JOptionPane.showMessageDialog(this, "Campos errados.");
        }

    }//GEN-LAST:event_btnEntrarActionPerformed

